# Data type

# string
name = 'naisan'
print(name)
# naisan

# num
age = 25
print(age)
# 25

# boolean
x = True
y = False
print(x)    # true
print(y)    # false

# list = array
li = [1, 2, 3, 4]
print(li)
# [1, 2, 3, 4]

# set
se = {1, 2, 3}
print(se)
# {1, 2, 3}

# tuple
tup = (1, 2, 3)
print(tup)
# (1, 2, 3)

# dictionary = object
di = {
    'name': 'naisan',
    'age': 25
}
print(di)
# {'name': 'naisan', 'age': 25}
