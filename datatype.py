# Data type

# num
age = 25
print(age)
# 25

# boolean
x = True
y = False
print(x)    # true
print(y)    # false

# set
se = {1, 2, 3}
print(se)
# {1, 2, 3}

# tuple
tup = (1, 2, 3)
print(tup)
# (1, 2, 3)

# dictionary = object
di = {
    'name': 'naisan',
    'age': 25
}
print(di)
# {'name': 'naisan', 'age': 25}
