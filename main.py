# input() - take input
name = input('what is your name: \n')   # \n use for new line
print(name)
# print input string

# len() - get length
full_name = 'naisan novel'
length = len(full_name)     # len func use for get length
print(length)
# 12

# float() - get floating num
age = 24
print(float(age))
# 24.0

# int() - get int num
total = 150.25
print(int(total))
# 150
